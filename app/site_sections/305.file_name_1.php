<p>Водяной воздухоохладитель — это теплообменник для охлаждения приточного воздуха в системах вентиляции. В качестве хладагента в нем чаще всего используется холодная вода или гликолевые растворы. Как правило, охладители приточных установок делаются из меди и алюминия.</p>
<p>Почти все охладители для вентиляции из нашей практики — это мощные многорядные аппараты, которые проектируются в индивидуальном порядке, под заказ. Купить готовый воздухоохладитель для промышленной установки можно далеко не всегда, так как универсальных решений тут практически нет. Размеры, рядность, мощность охладителя воздуха рассчитываются под конкретную приточную установку.</p>
<p>По тем же причинам цена охладителя рассчитывается под заказ после окончательного подбора.</p>

