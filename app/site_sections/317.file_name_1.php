<p> Теплообменники TPS с медными трубками 12 и 16 мм производятся в Словении. Максимальные размеры:<ul>
<li>длина: 9 метров;
<li>высота: 3 метра.</ul></p>
<p> Ламели могут быть медными, алюминиевыми с эпоксидным покрытием или без него.</p>
<p> Корпус теплообменника изготавливается из меди, алюминия, оцинкованной стали (с эпоксидным покрытием без него) или нержавеющей стали марок AISI 304 \316.</p>
<p> Варианты подключения: резьбовое, фланцевое, стальной гребень.</p>
<p> Под заказ теплообменник может быть укомплектован каплеуловителем, аксессуарами для трубопроводной установки, покрыт порошковой краской или защитным составом.</p>
