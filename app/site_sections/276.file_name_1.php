<p>Главный плюс французских дизель-электростанций &ndash; в понятной и предсказуемой автоматике. Система нуждается в операторе, но за многим следит сама: настраивает &laquo;штатные&raquo; параметры, смотрит за безопасностью в сети. Может поддержать работоспособность и в помещении, и в экстремальных условиях &ndash; на открытом воздухе, при максимальной загрузке, во время скачков напряжения.</p>

