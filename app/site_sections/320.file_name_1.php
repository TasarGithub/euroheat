<p><h3> Преимущества</h3></p>
<p>
<li> полная укомплектованность установок необходимыми устройствами для обработки воздуха (вентилятор, фильтр, воздухонагреватель, воздухоохладитель, увлажнитель, холодильная машина при необходимости, смесительные камере, теплоутилизаторы, шумоглушители и др.);</li>
<li> высокая экономичность обработки воздуха, обеспечиваемая разработкой и конструированием установки индивидуально для каждого заказчика;</li>
<li> разнообразие исполнений: горизонтальные (плоские) установки предназначены для помещений с дефицитом высоты; выпускаются модификации с наружной установкой (на кровле здания);</li>
<li> обеспечивается экологическая безопасность оборудования;</li>
<li> установки укомплектовываются полным набором средств автоматического регулирования;</li>
<li> каркасно-панельные конструкции установок позволяют сократить трудоемкость сборочных операций при монтажно-демонтажных работах;</li>
<li> уменьшены затраты времени и средств на проектирование установок - имеется программа подбора оборудования на компьютере.</li></p>
<h3 class="align-left">Конструкция</h3>
<p>В состав оборудования приточных установок и центральных кондиционеров
входят функциональные блоки различного назначения (для нагревания,
охлаждения, очистки, смешения воздуха, утилизации теплоты удаляемого из помещений
воздуха и др.), имеющие унифицированные присоединительные размеры и размещаемые в
корпусах для внутреннего монтажа типа <p> 
