Между угловым соединителем ставится резиновая прокладка-уплотнитель. Панели
присоединяются винтами так, чтобы детали крепежа не выступали внутрь установки. Внутренние поверхности установки остаются гладкими.
Соединение секций выполняется в углах с помощью жестких уголков или специальных клипс. В случае необходимости секции разделяются с помощью Т-образных боковых
профилей.
Стандартные размеры наших модулей 195, 240, 360, 550, 720, 920, 1200, 1650 мм имеются также промежуточные размеры. Съемные двери высотой до 1300 мм устанавливаются
при помощи наружного прижима. Свыше 1300 мм - устанавливаются на шарнирах <p> 
