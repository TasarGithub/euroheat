<h3 class="align-left"> Установки с толщиной стенки 45 мм</h3>
<p>Панели имеют двойные стенки из оцинкованной стали и заполнены негорючим, звукоизолирующим и теплоизолирующим стекловолоконным материалом толщиной 45 мм. Толщина внутреннего и наружного листов стали панели 0,8 - 1,0 мм; за дополнительную оплату
поставляется сталь толщиной 1,25 мм. Изоляция выполняется многослойной из минеральной
ваты с плотностью слоев: в поперечном направлении - 90 кг/м
