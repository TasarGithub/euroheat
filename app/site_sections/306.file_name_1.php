<p><b>Узел обвязки калорифера</b> предназначен для регулирования расхода теплоносителя и, соответственно, температуры воздуха на выходе из теплообменников приточных установок. Максимальная температура
теплоносителя для работы смесительного узла — это +150 C°, рабочее давление узла  — 0-10 Бар.</p>

<p>Элементы смесительного узла обвязки приточной вентиляции:</p>
<ul>
<li>Насос</li>
<li>Трехходовой клапан</li>
<li>Привод</li>
<li>Кран шаровый ручка</li>
<li>Обратный клапан</li>
<li>Фильтр сетчатый</li>
<li>Термоманометр (опция)</li>
    <li>Гибкие подводки (опция)</li>
</ul>

