<h3>Производство теплообменников в Италии </h3>
<p>«ЕвроКлима» — официальный дистрибьютор завода по производству теплообменного оборудования CO.MA. spa (Италия). Специализацией CO.MA. spa является изготовление на заказ теплообменников из нержавеющей стали для пищевой промышленности. Нержавеющая сталь INOX AISI 304 и INOX AISI 316 используется для изготовления рамы, трубок, проводящих пластин и ламелей теплообменника.</p><br>
<img src="/public/images/CoMa_1.jpg" class="img-responsive" ><br>
<p>Оребренные теплообменники CO.MA. spa изготавливаются в широком спектре материалов, геометрии и размеров и могут использоваться в разных режимах работы: вода, вода гликоля, перегретая вода, пар, хладагенты, диатермические масла, аммиак, воздух и пары.</p>
<p>Теплообменники Co.Ma. spa применяются в вентиляционных установках мировых лидеров производства климатического оборудования — таких, как AL-KO (Германия), WOLF (Германия), WESPER (Франция), Rhoss (Италия), Califel (Италия) и др. </p>
