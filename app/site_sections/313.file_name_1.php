<h4>Гарантия на теплообменники «вода-воздух» </h4><ul>
<li>гарантийный срок для медно-алюминиевых теплообменников — 2 года;
<li>гарантийный срок для аппаратов из нержавеющей стали — 5 лет. </ul>
<h4>Проверка герметичности теплообменников «вода-воздух» </h4>
<p>Все теплообменники «вода-воздух» проверяются на герметичность под давлением 23 атмосферы. Паровые теплообменники проверяются под давлением до 30 атмосфер. Испытания проходят в резервуаре с теплой водой. </p>
