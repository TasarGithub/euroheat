<p>Мы производим теплообменники «вода-воздух» с заданным количеством рядов:<ul>
<li>стандартные —  2 или 3 ряда;
<li>однорядные —  для второго контура догрева (вспомогательные теплообменники);
<li>многорядные (до 13 рядов) — для холодных регионов и воды с небольшой температурой, а также для охлаждения воздуха. </ul> </p>
<p>Теплообменники «вода-воздух» могут быть и нагревателями (калориферами), и охладителями воздуха. </p>

<p>Мы можем изготовить теплообменный аппарат: <ul>
<li>для приточной установки, 
<li>для центральных кондиционеров (кондиционеры центральные каркасно-панельные, КЦКП), тепловентиляторов,
<li>для сушильной камеры (лесозаготовка, прачечные и химчистки), 
<li>для покрасочной камеры,
<li> для драйкулеров (сухих градирен),
<li> для кондитерского производства – например, для камер сушки крахмала.
</ul> </p>
<br>

<img class="img-responsive" src="/public/images/voda_vozduh.jpg" />
<p>Проектируем теплообменники под любые рабочие режимы: <ul>
<li>расчетная температура приточного воздуха — от -35°С;
<li>расчетная температура воздуха на выходе — до +25°С...+27°С;
<li>расчетная температура греющей среды — практически без ограничений, как типовая, так и нестандартная. </ul> </p>
<p>С какими нетиповыми теплоносителями мы работаем: <ul>
<li>перегретая вода — до +150°С;
<li>вода со сниженной температурой — до +60°С на входе.</ul> </p>
<p>При +150°С давление воды (фактически это уже пар) на стенки теплообменника изнутри будет избыточным. В этом случае мы предлагаем теплообменники из нержавеющей стали взамен медно-алюминиевых. У них значительно выше предел выносливости и длительной прочности. Служат нержавеющие теплообменники до 15 лет. </p>
<p>Во втором случае мы предлагаем нагреватели с большей площадью теплообмена. Такие калориферы точно будут выдерживать необходимый рабочий режим для воздуха. </p>
<p>Для эффективного использования поверхности теплообмена с учетом воздушного сопротивления мы подберем оптимальный шаг между ламелями. </p>
<p>Для сбора конденсата водяной охладитель может быть оборудован поддоном и каплеуловителем. </p>
