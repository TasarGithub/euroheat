<ul class="ul_arial">
<li>водяные (нагреватели, охладитель) - 23 атмосферы;</li>
<li>паровые - минимум 30 атмосфер;</li>
<li>фреоновые (испарители, конденсаторы) - минимум 40 атмосфер.</li>
</ul>

