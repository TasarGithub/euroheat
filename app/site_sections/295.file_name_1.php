<p>Водяной калорифер - это медно-алюминиевый теплообменник, предназначенный для нагрева приточного воздуха в системах вентиляции. </p>

<p>Мы производим:
<ul><li>водяные калориферы для приточной вентиляции;
<li>калориферы пар-воздух (паровые калориферы);
<li>промышленные калориферы под заказ, в строгом соответствии с вашим ТЗ.</ul></p>
<p>Калориферы для приточных установок производятся из меди и алюминия. Для более жестких рабочих режимов можно заказать стальной калорифер. Как производители нестандартного оборудования, мы делаем нагреватели под любую температуру рабочих сред.</p>
<p>Мы работаем с любыми формами ТЗ. Можем сделать вентиляционный калорифер по образцу, по чертежам, по температурно-расходным параметрам и габаритам. Привозить к нам образец не нужно — наши монтажники сами выезжают на замер.</p>
<p>Возможные диаметры трубок — 8, 10, 12, 16 мм. При необходимости сделаем калорифер длиной до 10 метров.</p>
<h4 class="align-left">Дополнительные опции:</h4>
<ul>
<li>ламели с эпоксидным покрытием;</li>
<li>ламели из алюминиево-магниевого сплава;</li>
<li>ламели из меди;</li>
<li>рама теплообменника из алюминия или нержавеющей стали;</li>
<li>медный коллектор уже в стандартном исполнении.</li>
</ul>
<p>Цена рассчитывается по запросу, сроки также называем при расчете. Как правило, время работы над калорифером — это 25-40 рабочих дней (в зависимости от материалов и сложности изготовления).</p>

