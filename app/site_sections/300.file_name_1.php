<p>Ламели на трубках могут быть из меди либо из алюминиево-магниевого сплава. Также мы делаем ламели с эпоксидным покрытием. Оно защищает от разрушения при суровых эксплуатационных режимах.</p> 

<p>По желанию заказчика возможно производство теплообменника с поддоном и каплеуловителем.</p> 
<p>Все изделия проверяются на герметичность сухим воздухом под давлением до 40 атмосфер в резервуаре с теплой водой.</p> 

