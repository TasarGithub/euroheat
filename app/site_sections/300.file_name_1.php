<p>Испаритель — это теплообменный аппарат для охлаждения приточного воздуха в системах вентиляции. Хладагентом является газ, который используется в системах холодоснабжения и кондиционирования — фреон.</p> 
<p>Мы делаем на заказ фреоновые испарители для вентиляции, в том числе для прямоугольных каналов. Если у вас нет подробных чертежей, только размеры и температура сред, мы спроектируем аппарат по вашему ТЗ.</p> 
<p>Мы предлагаем очень широкий диапазон диаметров трубок и геометрического расположения трубок. Под заказ делаем двухконтурное (и более) исполнение испарителя.</p> 
<p>Вместе с инженерным мы сразу представляем экономический расчет, сколько стоит испаритель.</p> 
<p>Два варианты рамы испарителя:<ul>
<li>алюминий;
<li>нержавеющая сталь INOX AISI 304 и INOX AISI 316. </ul></p> 
<p>Мы — официальные партнеры итальянского завода CO.MA. spa. На предприятии выпускаются теплообменники из нержавеющей стали высокого качества, в том числе и фреоновые испарители.</p> 

