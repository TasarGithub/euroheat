<p><b>Теплообменники из нержавейки</b> используются там, где под воздействием агрессивной окружающей или рабочей среды применение 
оборудования 
из стандартных материалов становится невозможным:<ul>
<li>в пищевой промышленности,
<li>в медицине,
<li>в судовых системах (например, в рефрижераторах на судах дальнего плавания).</ul></p>
<p>Существует два основных вида теплообменных аппаратов из нержавеющей стали:</p>
<br>
<h4 class="align-left">Теплообменник, изготовленный полностью из нержавейки</h4>
Применяется в условиях с агрессивной внешней средой: 
пищевая, химическая, атомная, нефтегазовая промышленность и т.д. 
<br> Практически не поддается коррозии.
<br>Может проработать до 20 лет.
<br>Является более дорогим вариантом, чем теплообменники из меди и алюминия.

<br>
<h4 class="align-left">Теплообменник с трубками из нержавеющей стали</h4>

Считается компромиссным вариантом, так как остальные элементы теплообменного аппарата изготовлены из стандартных 
материалов. Стоит в среднем вдвое дороже, чем теплообменник с трубками из меди и алюминия, но при этом дешевле, чем теплообменник целиком  из нержавеющей стали.
<br>В основном применяется в случаях, где агрессивной является 
рабочая среда теплообменника.  Также такое исполнение 
встречается в паровых калориферах, с высокой начальной температурой и, 
соответственно, высоким давлением пара.
</p>
<br>
