            else $link = '&nbsp;';
            
            # is_published
            if (empty($_[$i]['is_published'])) $trClass = ' class="item_hidden"';
            else unset($trClass);
            
            $rows[] = '
            <tr'.$trClass.'>
                <td class="center vertical_middle">
                    <a class="block" href="/control/feedbacks/?action=editItem&itemID='.$_[$i]['id'].'">
