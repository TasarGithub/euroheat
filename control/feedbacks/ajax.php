                    <th class="center vertical_middle">Отзыв</th>
                    <th class="center vertical_middle" style="width:100px;white-space:nowrap">Удаление</th>
                </tr>
                '.$rows.'
            </table>
            </div>
            ';
        }
        echo $result;
    }
    else echo 'По запросу &quot;'.$_POST['q'].'&quot; ничего не найдено.';
